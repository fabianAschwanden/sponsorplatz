package ch.sponsorplatz.organisation;

/**
 * Art einer Organisation auf der Plattform.
 */
public enum OrgTyp {
    /** Verein, Klub, Vereinigung. */
    VEREIN,
    /** Unternehmen / Firma als Sponsor-Org. */
    UNTERNEHMEN,
    /** Stiftung mit Förderzweck. */
    STIFTUNG,
    /** Sonstiges (Privat, gemeinnütziger Verband, …). */
    ANDERE
}
